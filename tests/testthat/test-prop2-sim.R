test_that("multiplication works", {
  set.seed(99)
  expect_identical(prop2_sim(0.9, 10), c(9L, 1L))
  set.seed(99)
  expect_identical(prop2_sim(0.9, 10), c(9L, 1L))
  expect_identical(prop2_sim(0.9, 10), c(10L, 0L))
  set.seed(99)
  expect_identical(prop2_sim(0.1, 10), c(1L, 9L))
  set.seed(99)
  expect_identical(prop2_sim(0.1, 10), c(1L, 9L))
  expect_identical(prop2_sim(0.1, 10), c(0L, 10L))
})
