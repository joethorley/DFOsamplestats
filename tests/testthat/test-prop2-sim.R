test_that("multiplication works", {
  set.seed(99)
  expect_identical(prop2_sim(0.9, 0.1, 10), c(9L, 0L))
  set.seed(99)
  expect_identical(prop2_sim(0.9, 0.1, 10), c(9L, 0L))
  expect_identical(prop2_sim(0.9, 0.1, 10), c(9L, 1L))
  set.seed(99)
  expect_identical(prop2_sim(0.1, 0.9, 10), c(1L, 9L))
  expect_identical(prop2_sim(0.1, 0.9, 10), c(1L, 5L))
  set.seed(99)
  expect_identical(prop2_sim(0.01, 0.09, 10), c(0L, 0L))
  set.seed(99)
  expect_identical(prop2_sim(0.45, 0.55, 10), c(5L, 4L))
  set.seed(99)
  expect_identical(prop2_sim(0.005, 0.0075, 1000), c(5L, 4L))
})
